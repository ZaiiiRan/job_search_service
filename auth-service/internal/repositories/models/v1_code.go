package models

import (
	"time"

	"github.com/ZaiiiRan/job_search_service/auth-service/internal/domain/code"
)

type V1CodeDal struct {
	Id              int64     `db:"id" json:"id"`
	UserId          int64     `db:"user_id" json:"user_id"`
	Code            string    `db:"code" json:"code"`
	GenerationsLeft int       `db:"generations_left" json:"generations_left"`
	ExpiresAt       time.Time `db:"expires_at" json:"expires_at"`
	CreatedAt       time.Time `db:"created_at" json:"created_at"`
	UpdatedAt       time.Time `db:"updated_at" json:"updated_at"`
}

func V1CodeDalFromDomain(c *code.Code) V1CodeDal {
	if c == nil {
		return V1CodeDal{}
	}

	return V1CodeDal{
		Id:              c.Id(),
		UserId:          c.UserId(),
		Code:            c.Code(),
		GenerationsLeft: c.GenerationsLeft(),
		ExpiresAt:       c.ExpiresAt(),
		CreatedAt:       c.CreatedAt(),
		UpdatedAt:       c.UpdatedAt(),
	}
}

func (c V1CodeDal) IsNull() bool { return false }
func (c V1CodeDal) Index(i int) any {
	switch i {
	case 0:
		return c.Id
	case 1:
		return c.UserId
	case 2:
		return c.Code
	case 3:
		return c.GenerationsLeft
	case 4:
		return c.ExpiresAt
	case 5:
		return c.CreatedAt
	case 6:
		return c.UpdatedAt
	default:
		return nil
	}
}

func (c V1CodeDal) ToDomain() *code.Code {
	return code.FromStorage(
		c.Id, c.UserId,
		c.Code, c.GenerationsLeft,
		c.ExpiresAt, c.CreatedAt, c.UpdatedAt,
	)
}
